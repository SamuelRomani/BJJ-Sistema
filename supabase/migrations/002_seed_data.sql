    '00000006-0000-0000-0000-000000000001',
    '00000006-0000-0000-0000-000000000002',
    '00000006-0000-0000-0000-000000000003',
    '00000006-0000-0000-0000-000000000004'
  ];
  d date;
  a uuid;
  t uuid;
  prob float;
begin
  for i in 0..44 loop
    d := current_date - i;
    -- Pula finais de semana com 60% de chance
    if extract(dow from d) in (0, 6) and random() < 0.6 then continue; end if;
    foreach a in array aluno_ids loop
      -- Probabilidade variada por aluno (simula frequência irregular)
      prob := case
        when a = '00000005-0000-0000-0000-000000000001' then 0.75
        when a = '00000005-0000-0000-0000-000000000002' then 0.55
        when a = '00000005-0000-0000-0000-000000000003' then 0.85
        when a = '00000005-0000-0000-0000-000000000004' then 0.60
        when a = '00000005-0000-0000-0000-000000000009' then 0.20  -- em risco de churn
        when a = '00000005-0000-0000-0000-000000000010' then 0.90
        else 0.65
      end;
      if random() < prob then
        t := turma_ids[1 + floor(random() * array_length(turma_ids, 1))::int % array_length(turma_ids, 1)];
        insert into checkins (turma_id, aluno_id, data, hora_checkin)
        values (t, a, d, '19:30:00')
        on conflict (turma_id, aluno_id, data) do nothing;
      end if;
    end loop;
  end loop;
end;
